package com.example.chatapp.security;

import io.jsonwebtoken.Claims;
import io.jsonwebtoken.Jwts;
import io.jsonwebtoken.security.Keys;
import org.springframework.stereotype.Component;

import java.security.Key;
import java.util.Date;

@Component
public class JwtUtil {

    private final String secret;
    private final Key key;
    private final long EXPIRATION_TIME = 864_000_000;

    public JwtUtil() {
        String envSecret = System.getenv("SECRET_KEY");
        if (envSecret != null) {
            this.secret = envSecret;
        } else {
            this.secret = System.getProperty("SECRET_KEY");
        }
        if (this.secret == null || this.secret.getBytes().length < 32) {
            throw new IllegalStateException("SECRET_KEY must be at least 32 bytes long");
        }
        this.key = Keys.hmacShaKeyFor(secret.getBytes());
    }

    public String generateToken(String username) {
        return Jwts.builder()
                .setSubject(username)
                .setIssuedAt(new Date())
                .setExpiration(new Date(System.currentTimeMillis() + EXPIRATION_TIME))
                .signWith(key)
                .compact();
    }

    public String validateTokenAndGetUsername(String token) {
        Claims claims = Jwts.parserBuilder()
                .setSigningKey(key)
                .build()
                .parseClaimsJws(token)
                .getBody();
        return claims.getSubject();
    }
}
